    private void buscarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarButtonActionPerformed
        // TODO add your handling code here:
        if(txtRun.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Debe escribir el rut del Usuario", "Error al buscar", JOptionPane.WARNING_MESSAGE);
        } else {
            Usuario unUsuario = obtenerDB().usuarioBuscarPorRut(txtRun.getText());
            if(unUsuario==null){
                JOptionPane.showMessageDialog(rootPane, "No existe usuario", "Error al buscar", JOptionPane.WARNING_MESSAGE);
            } else {
                System.out.println(unUsuario);
                ArrayList <Telefonia> telefonosUsuario = obtenerDB().telefonosBuscarPorUsuario(unUsuario);
                if(telefonosUsuario.isEmpty()){
                    JOptionPane.showMessageDialog(rootPane, "No hay teléfonos para este usuario", "Error al buscar", JOptionPane.WARNING_MESSAGE);                
                } else {
                
                    DefaultTableModel modeloTable = (DefaultTableModel) cuentaTable.getModel();
                    Iterator it = telefonosUsuario.iterator();
                    int rowCount = modeloTable.getRowCount();
                    for (int i = rowCount - 1; i >= 0; i--) {
                        modeloTable.removeRow(i);
                    }
                    while(it.hasNext()){
                        Telefonia unTelefono = (Telefonia)it.next();
                        //Identificar que tipo de teléfono es y los datos según correspondan a dicho tipo
                        String tipo = "";
                        if(unTelefono instanceof Privado){
                            System.out.println("Es Privado");    
                            tipo = "Privado";
                        } else if (unTelefono instanceof Comerciales){
                            System.out.println("Es Comercial");
                            tipo = "Comercial";
                        } else if( unTelefono instanceof Tradicional){
                            tipo ="Móvil Tradicional";
                        } else if (unTelefono  instanceof IOS){
                            tipo = "iOS";
                        } else if (unTelefono instanceof Android){
                            tipo = "Android";
                        }
                        
                        modeloTable.addRow(new Object[]{unTelefono.getUsuario().getRun(),unTelefono.getUsuario().getNombre(),unTelefono.getUsuario().getApellido(),unTelefono.getUsuario().getFechaNacimiento(),unTelefono.getUsuario().getEstadoCivil(),unTelefono.getNumeroFono(),unTelefono.totalAPagar(),unTelefono.getCompania().getNombre(),tipo,unTelefono.getPlanTelefonico().getNombrePlan(),unTelefono.getCantidadMinutosUsadosMovil(),unTelefono.getCantidadMinutosUsadosFijos()});
                    }                
                }
            }
        }
    }//GEN-LAST:event_buscarButtonActionPerformed

